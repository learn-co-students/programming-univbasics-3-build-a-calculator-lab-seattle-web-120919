

def addition(num1, num2)
sum  = num1 + num2
sum
end

def subtraction(num1, num2)
sub_sum = num1 - num2
sub_sum
end

def division(num1, num2)
quotient = num1 / num2
quotient
end

def multiplication(num1, num2)
times_sum = num1 * num2
times_sum
end

def modulo(num1, num2)
modulo_sum = num1 % num2
modulo_sum
end

def square_root(num)
square_root_num = Math.sqrt(num)
square_root_num
end
